public class SwitchCase {
    public static void main(String[] args) {
        String subject = "Frau mit Handy";

        switch (subject){
            case  "Mann mit Handy":
                System.out.println("Mann mit Telefon");
                break;
            case "Opa mit Handy":
                System.out.println("Opa hat Telefon rausgeholt");

            
        }


    }



}